//animation.css
function animationHover(element, animation) {
    element = $(element);
    element.hover(
        function () {
            element.addClass('animated ' + animation);
        },
        function () {
            //动画完成之前移除class
            window.setTimeout(function () {
                element.removeClass('animated ' + animation);
            }, 2000);
        });
}

//拖动面板
function WinMove() {
    var element = "[class*=col]";
    var handle = ".ibox-title";
    var connect = "[class*=col]";
    $(element).sortable({
            handle: handle,
            connectWith: connect,
            tolerance: 'pointer',
            forcePlaceholderSize: true,
            opacity: 0.8,
        })
        .disableSelection();
};
/**
 * 生成分页信息
 * @param pageNum
 * @param totalPageNum
 * @returns {string}
 */
function makePageButton(pageNum, totalPageNum){
    var l = pageNum > 5 ? 5 : pageNum;
    var html = "";
    for(var i = 0; i < l - 1;i++){
        html += '<button class="btn btn-white page">'+(pageNum - l + i + 1)+'</button>';
    }
    html += '<button class="btn btn-white page active">'+pageNum+'</button>';
    if(pageNum < 10){
        for(var i = 0; i < (totalPageNum > 10 ? 10 : totalPageNum) - pageNum;i++){
            html += '<button class="btn btn-white page">'+(pageNum + i + 1)+'</button>';
        }
    }
    return html;
}

